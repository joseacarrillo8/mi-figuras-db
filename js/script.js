function loadCollection() {
  return JSON.parse(localStorage.getItem('myCollection')) || {};
}
function saveCollection(collection) {
  localStorage.setItem('myCollection', JSON.stringify(collection));
}
function loadReview() {
  return JSON.parse(localStorage.getItem('myReview')) || {};
}
function saveReview(review) {
  localStorage.setItem('myReview', JSON.stringify(review));
}
function loadPrices() {
  return JSON.parse(localStorage.getItem('myPrices')) || {};
}
function savePrices(prices) {
  localStorage.setItem('myPrices', JSON.stringify(prices));
}

function populateSagaFilter() {
  const filterSaga = document.getElementById('filterSaga');

  const uniqueSagas = Array.from(new Set(
    figures
      .filter(fig => fig.saga)
      .map(fig => fig.saga)
  )).sort();

  filterSaga.innerHTML = '<option value="all">Todas las sagas</option>';

  uniqueSagas.forEach(saga => {
    const option = document.createElement('option');
    option.value = saga;
    option.textContent = saga;
    filterSaga.appendChild(option);
  });
}

function populateYearFilter() {
  const filterYear = document.getElementById('filterYear');

  const uniqueYears = Array.from(new Set(
    figures
      .filter(f => f.year)
      .map(f => f.year)
  )).sort((a, b) => a - b);

  filterYear.innerHTML = '<option value="all">Todos los años</option>';

  uniqueYears.forEach(year => {
    const option = document.createElement('option');
    option.value = year;
    option.textContent = year;
    filterYear.appendChild(option);
  });
}

